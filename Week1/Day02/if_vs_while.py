# if condition runs once and ends if given condition is satisfied
print("First running if statement")
a = 0
if a < 5:
    print(f"value of a is {a}, it is less than 5")
    a = a + 1

# While loop is block of code that will execute over and over until the condition is satisfied
print("Now running while loop")
a = 0
while a < 5:
    print(f"Value of a is {a}")
    a = a + 1